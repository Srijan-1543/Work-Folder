number1 = float(input("First number: "))
number2 = float(input("\nSecond number: "))


sum = (number1) + (number2)
diff = (number1) - (number2)
prod = (number1) * (number2)
quo = (number1) / (number2)
rem = (number1) % (number2)

print("The sum of {0} and {1} is {2}" .format(number1, number2, sum))
print("The difference of {0} and {1} is {2}" .format(number1, number2, diff))
print("The product of {0} and {1} is {2}" .format(number1, number2, prod))
print("The quotient of {0} and {1} is {2}" .format(number1, number2, quo))
print("The reminder of {0} and {1} is {2}" .format(number1, number2, rem))
